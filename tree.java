class Tree {
String treeName;
String treeType;
boolean leavesFall;
String leafColor;
Tree(){
  treeName = "";
  treeType = "";
  leavesFall = false;
  leafColor = "";
}
Tree(String aTreeName, String aTreeType, boolean aLeavesFall, String aLeafColor){
  treeName = aTreeName;
  treeType = aTreeType;
  leavesFall = aLeavesFall;
  leafColor = aLeafColor;
}

}






















  
